	void CCombinationLayoutUI::SetEnabled(bool bEnabled)
	{
		if (m_bEnabled == bEnabled) return ;
		for (int i = 0;i<GetCount();i++)
		{
			CControlUI* pControl = GetItemAt(i);
			if (pControl)
			{
				pControl->SetEnabled(bEnabled);
			}
		}
		CControlUI::SetEnabled(bEnabled);
		if( !IsEnabled() ) {
			m_uButtonState = 0;
		}
		Invalidate();
	}

